import math

#raiz
raiz_quadrada = math.sqrt(16)

#fatorial
fatorial = math.factorial(5)

#seno, cosseno, tangente
seno = math.sin(math.pi / 2)
cosseno = math.cos(math.pi)
tangente = math.tan(math.pi / 4)

log_natural = math.log(10)

log_base10 = math.log10(100)

print(raiz_quadrada, fatorial, seno, cosseno, tangente, log_natural, log_base10)

